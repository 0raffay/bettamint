                <h3>Kezya, what inspired you to join the Women in IT Summit Asia?</h3>
                <p>I’m thrilled to be part of the dialogue at the Women in IT Summit Asia, where the underlying focus is to drive action towards improving human and planetary outcomes while making way for more responsible economic models in technology. In a rapidly advancing digital world, it’s no longer enough for technology to be transformative; it must also be responsible and inclusive. </p>
                <p>You are taking part in the Impactful Ideas Stage: ESG-centric Technology. Very briefly, what will you be speaking about during the session? </p>
                <p>I am thrilled to have an opportunity to share Bettamint’s role in bridging the digital divide and creating an abundance of income opportunities for millions of digitally disadvantaged daily wage earners in the construction industry. And how in doing so, we not only serve the autonomous workers but also propel the industry, economy and country forward. We want to exemplify that social impact and scalable tech businesses are not only compatible but are, in fact, inseparable. </p>
                <h3>What do you hope delegates will leave with on the day as a consequence of attending?</h3>
                <p>With the global population facing multiple crises threatening our existence, combining intellectual rigour and technology to serve human needs is no longer a choice – it’s a necessity. I hope the Summit nurtures radical collaboration between technology action leaders, innovators, policymakers and investors. And that we are able to recognise that technology is merely an enabler collectively; it is up to us to steer it towards human betterment.</p>
