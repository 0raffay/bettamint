            <p>Despite high interest rates and non-stop price hikes, the Indian real estate, especially the housing sector has witnessed an unprecedented growth in 2023. Without an iota of doubt, the industry players are unanimous that growth trajectory will continue in 2024 as well. </p>

            <p>The country’s economy grew 7.6 per cent during the July-September quarter of the current financial year 2023-24 which was the highest in the world. Nifty Realty Index has surged by over 77 per cent so far this year. Sensex crossed 72,000 level to hit fresh all-time high and trend is still is bullish.</p>

            <h3>Residential sales</h3>

            <p>Establishing a 15-year high, residential sales across the top 7 cities stood at approximately 4,76,530 units in 2023 against 3,64,870 units in 2022 – a 31 per cent annual growth.</p>

            <p>New launches across the top 7 cities in 2023 saw 25% annual rise – from 3,57,640 units in 2022 to nearly 4,45,770 new units in 2023.</p>

            <h3>Luxury Housing</h3>

            <p>It would not be an exaggeration to say luxury housing was the realty flavor of 2023. During 2023, nearly 23% of total new supply was added in the price bracket of over rs 1.5 Crores, as against 17% in 2022. </p>

            <h3>Inventory </h3>

            <p>Strong sales in 2023 lead to sharp decline of inventory overhang; lowest since 2018. The inventory overhang declined to 15 months at the end of 2023 compared to 21 months, a year ago. </p>

            <h3>MMR Leads </h3>

            <p>Mumbai Metropolitan Region, MMR, continued to be the largest residential market both in terms of value as well as volumes. MMR witnessed highest new units launches of 1,57,700 units in 2023, contributing nearly 35 per cent of the total supply of top-7 cities. With nearly 1,53,870 units sold in 2023, the city witnessed an annual increase of 40 per cent. </p>

            <h3>Residential Prices </h3>

            <p>Northward journey of residential property prices continued, with a 10%-24% rise during the year, across the top 7 cities primarily due to an increase in the input costs and strong demand. </p>

            <h3>Housing Rentals </h3>

            <p>Housing rents continued to soar across the top 7 cities during 2023, led by the prominent IT-ITeS hubs of Bengaluru, Hyderabad and Pune. The annual rise in rental values in these markets were in the range of 15% to 30%. </p>

            <h3>Affordable Housing lagged </h3>

            <p>Affordable housing share continued to decline in new launches, with only 18% share in Pan India supply of 2023. </p>

            <h3>Commercial Real Estate</h3>

            <p>Commercial Real Estate showed remarkable resilience amidst global slowdown. Rebound of large deals in Q4-2023 pushed the net leasing for 2023 to approximately 38 Mn sq ft, which is 5% more than 2022 but short of the last peak of 2019. Global capability centres, particularly in the technology and BFSI sectors, accounted for almost 40% of the significant deals in the top seven cities. Demand from flex operators stood at 8.7 Mn Sq ft, marking a 24% growth compared to 2022. The IT-ITeS sector to office leasing grew from around 50% in 2020 to 25% in 2023. The sectoral contributions from BFSI and engineering and manufacturing sectors have almost doubled, increasing from 10%-12% in 2020 to around 16% -20% in 2023. </p>

            <h3>Retail</h3>

            <p>India’s retail sector witnessed significant growth with retail development taking place in tier II and III cities, along with metros. Year 2023 witnessed highest supply of Grade-A malls in last 8 years. In 2023, supply of malls in the top cities touched ~5 Mn Sq ft, with over 80% being Grade-A category. With limited mall supply, prominent retailers have been exploring well-known main streets, which has led to strong demand and positive lease activity in recent years. During the year, leasing activity in the prominent main streets increased by 3% to 4% on year. Average vacancy levels in superior malls continued to remain tight within the range of 8%-10%. Strong demand contributed to the average rental appreciation of 8%-10% Y-o-Y, which is anticipated to last until 2024. </p>

            <p>Realty pundits predict not only sustenance of this growth but a further surge. They attribute this optimism to expectations of a robust economic growth along with softening of interest rates. Post Covid, there has been a surge of home ownership concept. RERA too has a major contribution in restoring homebuyers’ confidence into the residential market. </p>

