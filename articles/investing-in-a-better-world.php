          <h3> In a scenario where real estate and massive infrastructure projects cause pollution in
            Mumbai, it is crucial to explore potential solutions to safeguard the city's air quality. </h3>
          <p>In a shocker for Mumbaikars,
            who were under the impression that
            proximity to the sea would take care
            of any pollution, Mumbai’s
            Guardian Minister Deepak
            Kesarkar, citing data from the
            Central Pollution Control Board,
            said the city had an average air
            quality index (AQI) score of 156
            while New Delhi was at 121. He
            attributed this to development
            projects such as the metro and
            real-estate construction. To curb
            pollution, Brihanmumbai Municipal
            Corporation (BMC) has warned that
            it will shut down construction sites
            that do not take pollution
            control measures. </p>
          <p>Currently, there are about 6,000
            construction sites in Mumbai,
            including road infrastructure,
            metro-rail and other real-estate
            projects. Also, it is possible that
            construction activity has more than
            doubled on account of the 50 per
            Investing in a Better World
            cent premium (fees for approval)
            waiver announced by the
            Maharashtra government in 2021?
            In January 2021, the state had
            approved a proposal to slash
            premium charges on real-estate
            projects by half until
            31 December 2021. </p>
          <p>A vital factor that has contributed
            to the deteriorating air quality is the
            fact that the city has lost its green
            cover by 40 per cent between 1991
            and 2022 and 30 per cent of its
            water bodies owing to rampant
            modernisation and construction
            activities. Over the past few years, a
            substantial number of trees, which
            play a vital role in mitigating air
            pollution by capturing suspended
            particulate matter, have been cut
            down to make way for real-estate
            and mega infrastructure projects.</p>
          <p>Vehicular emissions are also a
            significant contributor to Mumbai’s
            air pollution woes. Further, reports
            say refineries in the Chembur and
            Mahul areas are hotspots for air
            pollution. Besides, Deonar and
            Mankhurd have massive garbage
            dumps where people regularly burn
            garbage that lets out toxic fumes,
            polluting nearby areas.</p>
          <p>This deterioration in the city’s air
            quality did not happen overnight – it
            has been steadily increasing over
            the years. In fact, Mumbai’s air
            pollution increased by 60 per cent
            from 2019 to 2023 (See table). </p>
          <h3>Air pollution </h3>
          <p>The World Health Organisation
            (WHO) estimates that air pollution
            kills 4.2 million people around the
            world every year. About 56 per cent
            of occupational cancer in men is
            from within the construction
            industry. Also due to the scale of
            many construction projects,
            equipment is often running and
            polluting for a long time.</p>
          <p>Construction machines generate
            around 400 MT of CO2 emissions
            per year. In fact, the construction
            industry in the UK is responsible for
            the largest annual number of
            occupational cancer cases with
            around 8 per cent of these directly
            related to diesel engine exhaust
            emissions. Further, air pollution
            contributes significantly to global
            warming and, therefore, to
            climate change. </p>
          <h3>Water pollution </h3>
          <p>Construction activities often
            involve the use of contaminants in
            the form of toxic chemicals and
            pollutants, such as volatile organic
            compounds (VOCs), paints, glues,
            diesel, oils, other toxic chemicals
            and cement, which may end up in
            water bodies through drains, seep
            into soil or run off directly into rivers
            or lakes. Some VOCs, including
            benzene, can increase the risk of
            cancer if swallowed or breathed in
            large amounts over a long period
            of time. Pollutants from construction
            sites can soak into the groundwater.
            Many places use the dark murky
            water for drinking purposes and
            even crops get adversely affected. It
            is difficult to treat groundwater and
            chemical pollutants such as arsenic
            and mercury in drinking water can
            cause health issues, including
            cancer. Water contamination can
            also harm or kill fish and animals. </p>
          <h3>Noise pollution </h3>
          <p>The different types of heavy-duty
            machinery used in excavation,
            demolition, building and
            landscaping all contribute to noise
            pollution. The fact that we can’t
            ‘see, taste or smell’ sounds may
            explain why it has not received as
            much attention as other types of
            pollution. WHO reported an
            estimated 1 million healthy years of
            life lost every year from effects of
            environmental noise, including
            annoyance, sleep disturbance and
            ischemic heart disease.</p>
          <h3>Industry speak </h3>
          <p>On the decision taken by the
            Government to ban construction
            and demolition activities in areas
            with severe air quality, <strong> Ankush
            .aul, Chief Business Offcer,
            Ambience Group </strong>, says, “We fully
            support the Government's initiative
            to pause construction activities in an
            effort to combat pollution. We
            understand that this decision is in
            the best interest of public health and
            environmental sustainability. While
            this temporary halt may pose
            challenges to our industry, we firmly
            believe in prioritising the well-being
            of our community. We are prepared
            to take all necessary steps to
            contribute to a cleaner, healthier
            Delhi NCR.”</p>
          <p>Meanwhile, <strong> Anuj Puri,
            Chairman, ANAROCK Group </strong>,
            says, “Amid Mumbai’s worsening air
            quality, BMC is mulling a halt of
            construction activities, particularly
            for those projects that do not adhere
            to proper dust and pollution control
            measures. If this continues, it will
            definitely affect the timely delivery of
            projects. As far as construction is
            concerned, our research indicates
            that there are currently as many as
            nearly 0.4 million housing units
            under various stages of construction
            in Mumbai alone. All these units
            have been launched in 2013 and
            beyond. On an average, a onemonth ban on all construction
            activity delays a project by at least
            three to four months.” </p>
          <p>As for air monitoring measures to
            be carried out at construction and
            demolition sites, <strong> Dhaval Monani,
            Director, Affordable Housing,
            Anant National University </strong>,
            explains, “Construction sites emit
            many major pollutants, PM 10 and
            2.5 being the major ones due to
            excavation, material handling and
            preparation. In addition, CO2,
            VOCs and NOx are produced
            because of burning of fuels and
            activities such as welding. PM
            monitoring sensors are the best
            indicators. Depending on site
            conditions and design, a minimum
            number should be made
            mandatory. Sensors to measure
            CO2, monoxide, volatile organic
            chemicals and nitrous oxide
            emissions – major soil pollutants
            and a cause of respiratory problems
            – are expensive and usually
            avoided. These monitors should be
            installed as the effects of these
            chemical pollutants last longer.” </p>
          <p>The global construction sector
            contributes to 23 per cent of air
            pollution, 40 per cent of drinking
            water pollution and 50 per cent of
            landfill waste. Queried about
            learnings from other countries in
            terms of pollution, he says,
            “Western countries have never had
            such a volume of construction as we see in India today. europe
            extensively uses noise barriers and
            very high level of mechanisation
            and tight control over working
            hours. Water pollution has been the
            most difficult to tackle as it is difficult
            to assess. europe has very stringent
            laws with random inspections that
            have proved most effective with
            builders or contractors taking
            proactive steps to reduce pollution.”</p>
          <p>About metro construction and
            real-estate projects, Monani says,
            “Mumbai is seeing AQI of over 240
            in certain areas like BKC and South
            Mumbai, which are construction
            hotspots. The coastal road, metro
            works and roadworks are going on
            at an unprecedented pace,
            coinciding with a bull market in
            construction. Metro and road
            projects, in particular, contribute to
            much higher pollution as a very
            large amount of excavation needs
            to be done. The coastal road is
            along the western sea face and
            winds carry extra particulate
            pollution in certain areas. There are
            limited options for the Government.
            It cannot slow down metro or road
            construction nor can it put
            unreasonable demands on private
            developers. The only options are to
            spray water to keep particulate
            matter down and ensure that sand
            and aggregate are sprayed with
            water before they enter the air
            mist machines.” </p>
          <p>In terms of construction waste,
            the National Building Code (NBC)
            prescribes the manner in which it
            should be handled. A suggestion
            put forth by it mentions the
            promotion of use of agricultural and
            industrial waste, including
            construction and demolition waste
            in building construction, without
            compromising on the quality
            and safety. </p>
          <p>Also, construction and demolition
            businesses in the UK generated
            67.8 MT of non-hazardous waste in
            2018, of which the recovery rate
            was around 92.3 per cent. So what
            should be done for business houses
            in India to properly manage their
            construction waste? Monani says,
            “Indian waste is of very low quality.
            In construction and demolition
            waste, high-value items like steel,
            metal (wires and aluminium), wood
            and even plastics are usually
            completely salvaged. even whole
            bricks are scavenged, cleaned and
            reused. Construction and demolition
            waste in India is virtually bricks,
            concrete and plaster. It can be
            converted into aggregate and sand.
            Recycled material is not financially
            viable under normal circumstances
            as in a majority of locations it is
            more expensive to process than
            virgin material and, in some ways,
            inferior in quality. There have to be
            incentives in place from the
            Government or a conscious effort
            from business houses to use
            construction waste for a significant
            portion of their needs.”</p>
          <h3>Impact on construction workers </h3>
          <p>The construction sector is India's
            second-largest job generator after
            agriculture, employing an estimated
            70 million people, including
            unskilled workers who leave the
            countryside for cities in the hope of
            better lives. A knee-jerk ban on
            construction activity affects
            daily-wage workers who cannot
            afford to sit at home or get ill,
            especially when jobs are scarce.
            The Delhi Government has at times
            given some compensation to
            construction workers when bans
            are enforced, but it was not done
            this year. </p>
          <p>The impact of pollution
            represents a heavy cost to the
            economy also. Lost labour income
            due to fatal illness from PM 2.5
            pollution in 2017 was in the range
            of $ 30-78 billion, equal in
            magnitude to about 0.3-0.9 per
            cent of the country’s GDP. While
            cement accounts for an astounding
            8 per cent of the world’s carbon
            dioxide emissions and pollution
            cannot be wished away, a ban on
            halting construction and allied
            infrastructure (metro, etc) activities
            in cities like Delhi affects daily wage
            workers for 10-15 days. </p>
          <p>“To address the economic impact
            of construction interruptions on
            wage workers, the Government has aunched welfare and security
            initiatives,” says <strong> Kezya De
            Bragança, Founder & CEO,
            Bettamint </strong>. "Both the Government
            and private sector must focus on
            enhancing awareness and literacy
            around these programmes, so that
            workers can access the necessary
            support. This collaboration is key to
            maintaining their livelihood during
            construction halts while relieving
            financial pressure on the private
            sector. In terms of health, beyond
            standard health checks and safety
            measures, construction workers
            need healthcare access that is both
            convenient and advanced.”</p>
          <p>As for measures to protect
            workers from pollution, she says,
            “Developed nations are pioneering
            the fight against pollution by
            adopting cutting-edge emission
            reduction techniques and
            incentivising the adoption of green
            technologies. Further, accessible
            healthcare technologies near
            worksites, combined with digital
            health monitoring and affordable
            credit, ensure workers' physical and
            financial well-being. In essence,
            innovative use of technology is
            central to developing sustainable
            construction practices that uphold
            environmental integrity and
            prioritise worker safety.” </p>
          <p>“It is imperative to administer
            regular air monitoring measures at
            construction sites to take care of the
            health of workers, site engineers
            and people living nearby,” affirms
            Saket Mohta, Managing Director,
            Merlin Group. About management
            of construction waste by business
            houses, <strong> Saket Mohta, MD, Merlin
            Group </strong>, advises, “establish a waste
            management team; set targets for
            waste reduction; select waste
            management partners and set
            clear goals.”</p>
          <h3>The way ahead </h3>
          <p>With 2023 poised to be the
            hottest year ever recorded, the UN
            Secretary General declared that this
            year marks the end of global
            warming and the onset of global
            boiling. even the most developed
            nations are not immune with floods
            in New York and Dubai serving as a
            reminder that mere words and
            pledges won't thwart the destructive
            power of the climate crisis. </p>
          <p>The Government has
            strengthened emission standards. In
            2020, it set aside about $ 1.7 billion
            to fight air pollution over the next
            five years for 42 Indian cities –
            provided they reduce their air
            pollution levels by 15 per cent every
            year. This is the world’s first
            performance-based fiscal transfer
            funding programme for air quality
            management in cities. Maharashtra
            too has earmarked 8 per cent of its
            total budget of Clean Air Funds,
            amounting to `992.2 million out of
            `12.4 billion, towards dust
            mitigation. It has also allotted
            80 per cent of the funds (`9.92
            billion) towards electrification of
            Mumbai’s public transport system. </p>
          <p>Construction and demolition are
            a crucial part of development and
            cannot be banned. So business
            houses should prevent ‘cost-cutting’
            activities and install emission control
            systems at construction sites to
            ensure particulate matter levels do
            not exceed prescribed limits.
            Studies show that continuous
            exposure to PM 2.5 pollutants has
            reduced the average lifespan of
            Mumbaikars by a few years, and
            tens of thousands of people die
            prematurely every year due to air
            pollution, but life goes on for the
            city’s residents. This ‘chalta hai’
            mindset must change. </p>
          <p>Air quality management also
            needs to be integrated into the
            behaviour of individuals as part of
            nation building. In Beijing (once the
            global capital of air pollution), Paris,
            London and Los Angeles, the need
            to address air pollution found
            resonance among the people, which
            assumed political significance and
            led to ambitious policymaking.
            Thus, people’s initiatives forced
            political action. Improving air
            quality requires concerted and
            collective efforts, apart from political
            will to take tough measures. </p>
          <p>So from the simple habit of
            switching off the car or two-wheeler
            ignition at traffic signals that exceed
            15 seconds, let us make the right
            (non-monetary) investments to
            create a better world for the
            next generation!</p>
